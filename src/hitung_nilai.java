    private void butNilaiTugasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butNilaiTugasActionPerformed
        // TODO add your handling code here:
        String inpNilaiTugas = nTugas.getText();
        if( inpNilaiTugas.length() != 0){
            int nTugasINT = Integer.parseInt(nTugas.getText());
            if(nTugasINT >= 0 && nTugasINT <= 39){
                kNilaiTugas.setText("E");
                hNilaiTugas.setText(inpNilaiTugas);
                hKetNilaiTugas.setText("E");
            }else if(nTugasINT >= 40 && nTugasINT < 70){
                kNilaiTugas.setText("D");
                hNilaiTugas.setText(inpNilaiTugas);
                hKetNilaiTugas.setText("D");
            }else if(nTugasINT >= 70 && nTugasINT < 80){
                kNilaiTugas.setText("C");
                hNilaiTugas.setText(inpNilaiTugas);
                hKetNilaiTugas.setText("C");
            }else if(nTugasINT >= 80 && nTugasINT < 90){
                kNilaiTugas.setText("B");
                hNilaiTugas.setText(inpNilaiTugas);
                hKetNilaiTugas.setText("B");
            }else if(nTugasINT >= 90 && nTugasINT <= 100){
                kNilaiTugas.setText("A");
                hNilaiTugas.setText(inpNilaiTugas);
                hKetNilaiTugas.setText("A");
            }else{
                JOptionPane.showMessageDialog(null, "Masukkan nilai tugas 0 - 100");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Nilai belum ditambahkan");
        }
    }//GEN-LAST:event_butNilaiTugasActionPerformed

