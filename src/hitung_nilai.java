    private void butHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butHitungActionPerformed
        // TODO add your handling code here:
         
         String tugas = nTugas.getText();
         String quiz = nQuiz.getText();
         String uts = nUTS.getText();
         String uas = nUAS.getText();
         
         if(tugas.length() != 0 &&
            quiz.length() != 0 &&
            uts.length() != 0 &&
            uas.length() != 0){
             
         
            int nTugasINT = Integer.parseInt(tugas);
            int nQuizINT = Integer.parseInt(quiz);
            int nUTSINT = Integer.parseInt(uts);
            int nUASINT = Integer.parseInt(uas);
            double resault = (nTugasINT + nQuizINT + nUTSINT + nUASINT) / 4 ;

            if(resault >= 0 && resault <= 39){
               ketHasil.setText("E");
               hasil.setText(Double.toString(resault));
            }else if(resault >= 40 && resault < 70){
               ketHasil.setText("D");
               hasil.setText(Double.toString(resault));
            }else if(resault >= 70 && resault < 80){
               ketHasil.setText("C");
               hasil.setText(Double.toString(resault));
            }else if(resault >= 80 && resault < 90){
               ketHasil.setText("B");
               hasil.setText(Double.toString(resault));
            }else if(resault >= 90 && resault <= 100){
               ketHasil.setText("A");
               hasil.setText(Double.toString(resault));
            }else{
               JOptionPane.showMessageDialog(null, "TIDAK LULUS");
            }
         }else{
             JOptionPane.showMessageDialog(null, "Lengkapi form terlebih dahulu");
         }
    }//GEN-LAST:event_butHitungActionPerformed

